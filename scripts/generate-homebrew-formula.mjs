#!/usr/bin/env node
/**
 * Generate a Homebrew formula that installs the prebuilt SEA CLI from GitHub Releases.
 *
 * Targets match `.github/workflows/binaries.yml` (macOS / Linux only; no Windows in Homebrew).
 *
 * Usage:
 *   GITHUB_REF_NAME=v1.2.3 node scripts/generate-homebrew-formula.mjs --output path/to/commentray.rb
 *   node scripts/generate-homebrew-formula.mjs --tag=v1.2.3 --stdout
 */
import { createHash } from "node:crypto";
import { mkdirSync, renameSync, writeFileSync } from "node:fs";
import { dirname } from "node:path";
import { fileURLToPath } from "node:url";

const REPO = "d-led/commentray";

/** @type {readonly { filename: string }[]} */
export const HOMEBREW_COMMENTRAY_ASSETS = [
  { filename: "commentray-darwin-arm64" },
  { filename: "commentray-darwin-x64" },
  { filename: "commentray-linux-arm64" },
  { filename: "commentray-linux-x64" },
];

/**
 * @param {{ version: string; sha256ByFilename: Record<string, string> }} opts
 * @returns {string}
 */
export function renderHomebrewCommentrayFormula({ version, sha256ByFilename }) {
  if (!/^[\w.-]+$/.test(version)) {
    throw new Error(`Expected a non-empty safe version string, got: ${version}`);
  }
  for (const { filename } of HOMEBREW_COMMENTRAY_ASSETS) {
    if (!sha256ByFilename[filename] || !/^[a-f0-9]{64}$/.test(sha256ByFilename[filename])) {
      throw new Error(`Missing or invalid sha256 for ${filename}`);
    }
  }

  const esc = (s) => s.replace(/\\/g, "\\\\").replace(/"/g, '\\"');

  const macArm = sha256ByFilename["commentray-darwin-arm64"];
  const macIntel = sha256ByFilename["commentray-darwin-x64"];
  const linuxArm = sha256ByFilename["commentray-linux-arm64"];
  const linuxIntel = sha256ByFilename["commentray-linux-x64"];

  return `# typed: false
# frozen_string_literal: true

# Generated by ${REPO} (scripts/generate-homebrew-formula.mjs). Do not edit by hand; update via CI or re-run the generator.
class Commentray < Formula
  desc "${esc("Commentray — paired commentary markdown CLI (standalone binary)")}"
  homepage "https://github.com/${REPO}"
  version "${esc(version)}"
  license "MPL-2.0"

  on_macos do
    on_arm do
      url "https://github.com/${REPO}/releases/download/v#{version}/commentray-darwin-arm64"
      sha256 "${macArm}"
    end
    on_intel do
      url "https://github.com/${REPO}/releases/download/v#{version}/commentray-darwin-x64"
      sha256 "${macIntel}"
    end
  end

  on_linux do
    on_arm do
      url "https://github.com/${REPO}/releases/download/v#{version}/commentray-linux-arm64"
      sha256 "${linuxArm}"
    end
    on_intel do
      url "https://github.com/${REPO}/releases/download/v#{version}/commentray-linux-x64"
      sha256 "${linuxIntel}"
    end
  end

  def install
    if OS.mac?
      if Hardware::CPU.arm?
        bin.install "commentray-darwin-arm64" => "commentray"
      else
        bin.install "commentray-darwin-x64" => "commentray"
      end
    elsif OS.linux?
      if Hardware::CPU.arm? && Hardware::CPU.is_64_bit?
        bin.install "commentray-linux-arm64" => "commentray"
      elsif Hardware::CPU.intel? && Hardware::CPU.is_64_bit?
        bin.install "commentray-linux-x64" => "commentray"
      else
        odie "Unsupported Linux CPU architecture"
      end
    else
      odie "Unsupported platform"
    end
  end

  test do
    assert_match version.to_s, shell_output("#{bin}/commentray --version")
  end
end
`;
}

/**
 * @param {string} tag
 * @param {(url: string) => Promise<ArrayBuffer>} fetchFn
 * @returns {Promise<Record<string, string>>}
 */
export async function sha256ReleaseAssets(tag, fetchFn = defaultFetch) {
  if (!tag.startsWith("v")) {
    throw new Error(`Expected tag like v1.2.3, got: ${tag}`);
  }
  const base = `https://github.com/${REPO}/releases/download/${tag}`;
  /** @type {Record<string, string>} */
  const out = {};
  for (const { filename } of HOMEBREW_COMMENTRAY_ASSETS) {
    const url = `${base}/${filename}`;
    const buf = Buffer.from(await fetchFn(url));
    out[filename] = createHash("sha256").update(buf).digest("hex");
  }
  return out;
}

async function defaultFetch(url) {
  const res = await fetch(url, { redirect: "follow" });
  if (!res.ok) {
    throw new Error(`GET ${url} failed: ${res.status} ${res.statusText}`);
  }
  return res.arrayBuffer();
}

/**
 * @param {string[]} argv
 * @param {number} i
 * @param {{ outputPath: string; stdout: boolean; tag: string }} state
 * @returns {number} index of last consumed argv element
 */
function applyCliArg(argv, i, state) {
  const a = argv[i];
  if (a === "--stdout") {
    state.stdout = true;
    return i;
  }
  if (a.startsWith("--output=")) {
    state.outputPath = a.slice("--output=".length);
    return i;
  }
  if (a === "--output") {
    state.outputPath = argv[i + 1] ?? "";
    return i + 1;
  }
  if (a.startsWith("--tag=")) {
    state.tag = a.slice("--tag=".length);
    return i;
  }
  if (a === "--tag") {
    state.tag = argv[i + 1] ?? "";
    return i + 1;
  }
  throw new Error(`Unknown argument: ${a}`);
}

function parseArgs(argv) {
  const state = { outputPath: "", stdout: false, tag: "" };
  for (let i = 2; i < argv.length; i++) {
    i = applyCliArg(argv, i, state);
  }
  let { outputPath, stdout, tag } = state;
  if (!tag) tag = process.env.GITHUB_REF_NAME ?? "";
  if (!tag) {
    throw new Error("Set GITHUB_REF_NAME (e.g. v1.2.3) or pass --tag=v1.2.3");
  }
  if (!stdout && !outputPath) {
    throw new Error("Pass --output <path> or --stdout");
  }
  if (stdout && outputPath) {
    throw new Error("Use only one of --stdout or --output");
  }
  return { outputPath, stdout, tag };
}

async function main() {
  const { outputPath, stdout, tag } = parseArgs(process.argv);
  const version = tag.startsWith("v") ? tag.slice(1) : tag;
  const sha256ByFilename = await sha256ReleaseAssets(tag.startsWith("v") ? tag : `v${tag}`);

  const body = renderHomebrewCommentrayFormula({ version, sha256ByFilename });

  if (stdout) {
    process.stdout.write(body);
    return;
  }

  const dir = dirname(outputPath);
  if (dir && dir !== ".") mkdirSync(dir, { recursive: true });
  const tmp = `${outputPath}.${process.pid}.tmp`;
  writeFileSync(tmp, body, "utf8");
  renameSync(tmp, outputPath);
  console.log(`Wrote ${outputPath}`);
}

const isMain = process.argv[1] === fileURLToPath(import.meta.url);
if (isMain) {
  main().catch((e) => {
    console.error(e);
    process.exit(1);
  });
}
